package ExpressionEvaluator;

/**
 * StackException class
 * 
 * @author Xinyang Zhou
 * @version 1.0
 */
public class StackException extends java.lang.RuntimeException {
    /**
     * A constructor of StackException with a message.
     * 
     * @param message The message of the StackException
     */
    public StackException(String message) {
        // Call super constructor with the parameter message.
        super(message);
    }
}
